#include <libft.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;

	while (s1[i] != '\0')
		i++;
	
	while (j < n && s2[i] != '\0')
	{
		s1(i + j) = s2[j];
		j++;
	}
	return(s1);
}

